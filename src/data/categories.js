// ── Утилиты для работы с деревом категорий ──

// Все подкатегории в плоском массиве (с ссылкой на родителя)
export const ALL_SUBCATEGORIES = CATEGORIES.flatMap(parent =>
  (parent.subs || []).map(sub => ({ ...sub, parentId: parent.id, parentName: parent.name }))
)

// Найти корневую рубрику по id
export function getCategory(id) {
  for (const cat of CATEGORIES) {
    if (cat.id === id) return cat
    const sub = (cat.subs || []).find(s => s.id === id)
    if (sub) return { ...sub, parentId: cat.id, parentName: cat.name }
  }
  return null
}

export function categoryName(id) {
  const c = getCategory(id)
  return c ? c.name : 'Без категории'
}

export function categoryIcon(id) {
  const c = getCategory(id)
  return c ? c.icon : 'doc'
}

export function categoryColor(id) {
  const c = getCategory(id)
  return c ? c.color : '#6b7280'
}

export function categoryDesc(id) {
  const c = getCategory(id)
  return c ? c.desc : ''
}

// Найти корневую рубрику, которой принадлежит подкатегория
export function getParentCategory(subId) {
  return CATEGORIES.find(cat => (cat.subs || []).some(s => s.id === subId)) || null
}

// Маппы всех подкатегорий в виде id → метаданные (быстрый lookup)
export const SUBCATEGORY_MAP = Object.fromEntries(
  ALL_SUBCATEGORIES.map(s => [s.id, s])
)

// Маппинг устаревших id статей на новые (для совместимости)
export const LEGACY_CATEGORY_MAP = {
  web: 'web-security',
  recon: 'useful-resources',
  crypto: 'cryptography',
  reverse: 'reverse-engineering',
  pwn: 'exploits',
  mobile: 'mobile-security',
  network: 'network-security',
  cloud: 'cloud-security',
  forensics: 'digital-forensics',
  social: 'useful-resources',
  devsecops: 'devsecops',
  threatint: 'threat-intel',
  iot: 'iot-security',
  redteam: 'red-team',
  blockchain: 'web-security',
  infra: 'infra',
  api: 'api-security',
}

// Нормализовать id категории (устаревший → новый)
export function normalizeCategory(id) {
  return LEGACY_CATEGORY_MAP[id] || id
}
